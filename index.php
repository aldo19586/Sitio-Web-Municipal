                <div class="head-info" style="text-align:justify;">
                    La historia de Barranca se remonta a la época preincaica bajo el nombre de Guamanmayo, que en
                    quechua significa «Gavilán de Río». Sobre el particular, el cronista español Pedro de Cieza de León,
                    a su paso por la localidad de Barranca en 1547, dijo «Valle de Guamán que en castellano quiere decir
                    “Río del Halcón”».<br>

                    La tradición cuenta que el antiguo pueblo de Guamanmayo estuvo situado entre los fundos del Potao y
                    el Molino, a lo largo del camino incaico, en cuya vera se encontraba el Tambo Real, servido por los
                    naturales, anota el gobernador español Vaca de castro en 1543. Durante la colonia se conocía con el
                    nombre de «Karanca», que después de tiempo se llamaría Barranca, nombre actual y definitivo.<br>
                    Se tiene poca información sobre los primeros pobladores de la ciudad, es de suponer que los antiguas
                    pobladores fueron grandes pescadores, horticultores que con el correr de los años fueron
                    perfeccionando la agricultura, ganadería, cerámica así como sus instrumentos de trabajo.<br>

                    Época Pre-Incaica
                    Las primeras comunidades aldeanas y pobladores barranquinos se remontan al Período Incaico Superior
                    1,543 a.c. Se caracterizaban por dedicarse a las actividades tales como la horticultura,
                    construcción de casas con terrazas y trabajaban en grupos en chacras. Sus instrumentos: de trabajo
                    utilizaban era la cerámica, el sedentarismo Y el tejido en algodón.<br>

                    Época Incaica
                    Se remonta a la Cultura Chimú, ya que se extendía desde la Costa Norte de Piura, y hasta cerca de
                    Lima por el Sur. En el valle del río Pativilca se construyó la Fortaleza de Paramonga.<br>

                    El Reino del gran Chimú contaba con un ejército organizado cuya misión era garantizar el orden
                    dentro y fuera de su territorio, Estos pueblos pagaban tributos con oro, plata, cobre, piedras
                    preciosas ya que los habitantes del Reino eran muy aficionados.<br>

                    Época de la Conquista
                    Los Tres señoríos, Tributarios del Reino de Pachacamac no quisieron someterse al Imperio. Así fue
                    como Barranca, Huaura y Chancay iniciaron una resistencia contra las tropas incaicas, La lucha duró
                    dos años, murió mucha gente por ambos bandos, pero al fin el poder del Ejército Incaico consiguió la
                    victoria.<br>

                    Época de la Independencia
                    Según el viajero francés Julien Mellet, en la etapa previa a la Independencia Barranca era una gran
                    aldea habitada por muchos tributarios de carácter sumiso y dócil que le permitía tener gran comercio
                    de corderos y ganado vacuno, como también de carbón que se transportaba en gran cantidad, en mulas a
                    la capital. Las haciendas de la época como la de Huayto, se dedicaban al cultivo de caña y
                    elaboración de azúcar, así como maíz, frijoles, camotes; todo ello a través de una abundante mano de
                    obra esclava.<br><br>
